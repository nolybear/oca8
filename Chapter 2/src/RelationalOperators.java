public class RelationalOperators
{
	public static void main(String[] args)
	{
		int x = 10, y = 20, z = 10;
		System.out.println(x < y);
		System.out.println(x <= y);
		System.out.println(x >= z);
		System.out.println(x > z);
	}
}
